/** 기준 좌표에서 가까운 순으로 정렬된 테슬라 슈퍼차저 목록 */
export function teslaSuperchargersNear(from: { lat: number; lng: number }): Station[] {
  return SEED.map((s) => toStation(s, from)).sort(
    (a, b) => (a.distanceKm ?? Infinity) - (b.distanceKm ?? Infinity),
  );
}

